#include<stdio.h>
int main()
{
    int a=10 ;
    printf("Starting value is %d\n",a);
    printf("preincrement value is %d\n",++a);
    printf("predecrement value is %d\n",--a);

    printf("postincrement value is %d\n",a++);
    printf("After postincrement value is %d\n",a);
    
    printf("postdecrement value is %d\n",a--);
    printf("After postdecrement value is %d\n",a);

    
    
}